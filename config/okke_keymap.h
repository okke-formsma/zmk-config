/* Maintain standard 5-key, half-row macros, below these utility macros and
 * automatically apply `MT(MOD_*, keycode)` mod-tap modifiers with e.g.
 * the `MODS_L()` and `MODS_R()` utility macros.
 */

#define MT_L(kc_0, kc_1, kc_2, kc_3, kc_4) \
        &mt MOD_LCTL, (kc_0),                    \
        &mt MOD_LGUI, (kc_1),                    \
        &mt MOD_LALT, (kc_2),                    \
        &mt MOD_LSFT, (kc_3),                    \
        (kc_4)
#define MODS_L(...) MT_L(__VA_ARGS__)

#define MT_R(kc_4, kc_3, kc_2, kc_1, kc_0) \
        (kc_4),                                  \
        &mt MOD_LSFT, (kc_3),                    \
        &mt MOD_LALT, (kc_2),                    \
        &mt MOD_LGUI, (kc_1),                    \
        &mt MOD_LCTL, (kc_0)
#define MODS_R(...) MT_R(__VA_ARGS__)

#define LT_L(kc_0, kc_1, kc_2, kc_3, kc_4) \
        &lt _SYS, (kc_0),                        \
        &lt _BLE, (kc_1),                        \
        (kc_2),                                  \
        &lt _I3, (kc_3),                         \
        (kc_4)
#define LYRS_L(...) LT_L(__VA_ARGS__)

#define LT_R(kc_4, kc_3, kc_2, kc_1, kc_0) \
        (kc_4),                                  \
        &lt _I3, (kc_3),                         \
        (kc_2),                                  \
        &lt _BLE, (kc_1),                        \
        &lt _SYS, (kc_0)
#define LYRS_R(...) LT_R(__VA_ARGS__)

